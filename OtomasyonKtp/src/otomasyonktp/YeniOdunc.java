    void uyeCombo() {
        try {
            Baglanti db = new Baglanti();
            ResultSet rs = db.baglan().executeQuery("select ad from uyeler");
            while (rs.next()) {
                cmbUyeAd.addItem(rs.getString("ad"));
            }
        } catch (Exception e) {
        }

    }
     void uyeSCombo() {
          cmbUyeSoyad.removeAllItems();
        try {
            Baglanti db = new Baglanti();
            ResultSet rs = db.baglan().executeQuery("select soyad from uyeler where ad = '" +cmbUyeAd.getSelectedItem().toString()+"'");
            while (rs.next()) {
                cmbUyeSoyad.addItem(rs.getString("soyad"));
            }
        } catch (Exception e) {
        }

    }

    void kitapCombo() {
        try {
            Baglanti db = new Baglanti();
            ResultSet rs = db.baglan().executeQuery("select kitapadi from kitaplar where raftami = 1");
            while (rs.next()) {
                cmbKitap.addItem(rs.getString("kitapadi"));
            }
        } catch (Exception e) {
        }

    }
    void yazarCombo() {
        cmbYazar.removeAllItems();
        try {
            Baglanti db = new Baglanti();
            ResultSet rs = db.baglan().executeQuery("select yazaradi from kitaplar where kitapadi = '"+cmbKitap.getSelectedItem().toString()+"'");
            while (rs.next()) {
                cmbYazar.addItem(rs.getString("yazaradi"));
            }
        } catch (Exception e) {
        }

    }

