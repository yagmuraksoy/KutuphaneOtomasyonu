    private void btnOduncOnayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOduncOnayActionPerformed
        // TODO add your handling code here:
        String kitap = cmbKitap.getSelectedItem().toString();
        String yazar = cmbYazar.getSelectedItem().toString();
        String uyead= cmbUyeAd.getSelectedItem().toString();
        String soyad = cmbUyeSoyad.getSelectedItem().toString();
        String oduncT = lblOdunc.getText();
        String iadeT = lblIade.getText();
        String K ="", U ="";
        
        if (kitap.equals("") || yazar.equals("") || uyead.equals("") || soyad.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Lütfen boş bırakmayınız");
        } else {
            try {
                Baglanti db = new Baglanti();
                ResultSet rsK = db.baglan().executeQuery("select kitapid from kitaplar where kitapadi = '"+kitap+"' and yazaradi = '"+yazar+"'");
                ResultSet rsU = db.baglan().executeQuery("select uyeid from uyeler where ad = '"+uyead+"' and soyad ='"+soyad+"'");
               
                if(rsK.next())
                 K = rsK.getString("kitapid");
               
                if(rsU.next())
                   U =rsU.getString("uyeid");
                
                int ek = db.baglan().executeUpdate("insert into odunc(kitapid, uyeid, adminid, odunctarih, iadetarih, iademi)"
                    + " values('" + K + "', '" + U + "', '" + AdminGiris.adminID +"', '"+ oduncT + "','"+iadeT+"','0')");
              db.baglan().executeUpdate("update kitaplar set raftami = 0, adminid = "+ AdminGiris.adminID+" where kitapid = "+ K);
                if (ek == 1) {
                    JOptionPane.showMessageDialog(rootPane, "İşlem Onaylandı");
                   
                }

            } catch (Exception e) {
                System.err.println("İşlem hatası:" + e);
            }
        }
    }//GEN-LAST:event_btnOduncOnayActionPerformed

