    void OduncKontrol() {
        String kitapad = "";
        String uyemail = "";
        try {
            Baglanti db = new Baglanti();
            ResultSet r = db.baglan().executeQuery("select * from odunc where iademi = 0");
            while (r.next()) {
                String iT = r.getString("iadeTarih");

                ResultSet rU = db.baglan().executeQuery("select * from uyeler where uyeid = " + r.getInt("uyeid"));
                while (rU.next()) {
                    uyemail = rU.getString("mail");
                }

                ResultSet rK = db.baglan().executeQuery("select * from kitaplar where kitapid = " + r.getInt("kitapid"));
                while (rK.next()) {
                    kitapad = rK.getString("kitapadi");
                }

                SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
                Date d = formatter.parse(iT);
                Calendar c = Calendar.getInstance();
                c.setTime(d);
                Calendar a = Calendar.getInstance();
                a.add(Calendar.DATE, 1);
                Calendar b = Calendar.getInstance();
                b.add(Calendar.DATE, 3);
                String msg = "Almış olduğunuz " + kitapad + " isimli kitabın iade tarihi yaklaşmıştır. Lütfen iade tarihi geçmeden iade ediniz. Aksi takdirde borç yazılacaktır.";

                if (c.compareTo(a) == 1 && c.compareTo(b) == -1) {
                    MailAt.mailAt(uyemail, "Kitap iadesi hk.", msg);
                }

            }
        } catch (Exception e) {
            System.err.println("İşlem hatası:" + e);
        }
    }

    void borcKontrol() {
        int borcgun = 0;
        try {
            Baglanti db = new Baglanti();
            ResultSet r = db.baglan().executeQuery("select * from odunc where iademi = 0");
            while (r.next()) {
                String iT = r.getString("iadeTarih");
                SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
                Date d = formatter.parse(iT);
                Calendar c = Calendar.getInstance();
                c.setTime(d);
                Calendar now = Calendar.getInstance();
                int a = now.compareTo(c); //c gerideyse ve aynıysa 1, ilerideyse -1

                if (a == 1) {
                    c.add(Calendar.DATE, 1);
                    while (a == 1) {
                        borcgun += 1;
                        c.add(Calendar.DATE, 1);
                        a = now.compareTo(c);
                    }

                    db.baglan().executeUpdate("update uyeler set borcgun =" + borcgun + " where uyeid = '" + r.getString("uyeid") + "'");
                }

            }
        } catch (Exception e) {
            System.err.println("İşlem hatası:" + e);
        }
    }


