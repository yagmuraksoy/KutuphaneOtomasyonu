    private void btnKSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKSilActionPerformed
        int sira = tblKitap.getSelectedRow();
        String yer = (String) tblKitap.getValueAt(sira, 5);
        try {
            if (yer != null) {
                Baglanti db = new Baglanti();
                ResultSet o = db.baglan().executeQuery("select odunc.kitapid from kitaplar inner join odunc on kitaplar.kitapid = odunc.kitapid where yer='" + yer + "'");

                if (o.next()) {
                    JOptionPane.showMessageDialog(rootPane, "Kitap Ödünç Olduğu için Silinemez!");
                } else {
                    db.baglan().executeUpdate("delete from kitaplar where yer='" + yer + "'");

                    JOptionPane.showMessageDialog(rootPane, "Kitap Silindi");
                    kitapListele();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Kitap Seçilmedi");
            }
        } catch (Exception e) {
            System.err.println("İşlem hatası:" + e);
        }
    }//GEN-LAST:event_btnKSilActionPerformed

